"""Abstract handle for interacting with a vector store collection."""

from __future__ import annotations

import abc
from types import TracebackType
from typing import Any


class VectorStoreHandle(abc.ABC):
    """Backend-agnostic handle returned by ``AbstractVectorStoreDataset.load()``.

    The handle owns the connection to a specific collection. Callers are
    responsible for closing it, either explicitly via ``close()`` or by using
    the handle as a context manager.

    Subclasses must implement all abstract methods.  The ``raw_client``
    property provides an escape hatch to the backend's native client object
    for operations not covered by this interface.
    """

    @abc.abstractmethod
    def add(self, records: list[dict[str, Any]]) -> list[str]:
        """Insert records into the collection.

        Args:
            records: Each record is a dict with keys:
                - ``"properties"`` (optional): payload dict stored alongside
                  the vector. Defaults to an empty dict when omitted.
                - ``"vector"`` (optional): embedding as a list of floats.
                  Required unless the collection has a server-side vectorizer.
                - ``"id"`` (optional): string UUID; generated by the backend
                  when omitted.

        Returns:
            List of IDs (strings) for the inserted records, in the same order
            as ``records``.
        """

    @abc.abstractmethod
    def search(
        self,
        *,
        vector: list[float] | None = None,
        text: str | None = None,
        top_k: int = 10,
        filters: Any = None,
    ) -> list[dict[str, Any]]:
        """Return the ``top_k`` nearest neighbours.

        Exactly one of ``vector`` or ``text`` must be provided.  This
        constraint is not enforced at the ABC level: each concrete backend
        is responsible for raising if neither or both are given.

        Args:
            vector: Query embedding.  The backend performs an ANN search.
            text: Query string.  Requires a server-side vectorizer configured
                on the collection.
            top_k: Maximum number of results to return.
            filters: Backend-native filter object (not portable across
                backends).

        Returns:
            List of result dicts.  Each dict contains at minimum:
            ``"id"`` (str), ``"properties"`` (dict).  May also include
            ``"vector"`` (list[float]) and ``"distance"`` (float).

        Note:
            Backends that only support one search mode (e.g. no server-side
            vectorizer for text search) should raise ``NotImplementedError``
            for the unsupported mode rather than silently ignoring it.
        """

    @abc.abstractmethod
    def delete(
        self,
        *,
        ids: list[str] | None = None,
        filters: Any = None,
    ) -> None:
        """Delete records from the collection.

        Exactly one of ``ids`` or ``filters`` must be provided.  This
        constraint is not enforced at the ABC level: each concrete backend
        is responsible for raising if neither or both are given.

        Args:
            ids: List of record IDs to delete.
            filters: Backend-native filter; deletes all matching records.
        """

    @abc.abstractmethod
    def describe(self) -> dict[str, Any]:
        """Return a summary of the collection (name, record count, config).

        Returns:
            A plain dict with at least ``"collection"`` and ``"count"`` keys.
        """

    @property
    @abc.abstractmethod
    def raw_client(self) -> Any:
        """The underlying backend client, for operations outside this interface."""

    @abc.abstractmethod
    def close(self) -> None:
        """Release the connection held by this handle.

        Must be idempotent: calling ``close()`` more than once must be
        harmless.
        """

    def __enter__(self) -> VectorStoreHandle:
        return self

    def __exit__(
        self,
        exc_type: type[BaseException] | None,
        exc_val: BaseException | None,
        exc_tb: TracebackType | None,
    ) -> None:
        self.close()
